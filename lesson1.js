//Declaring a variable
// You can use either var, let, const

let motivation="Javascript is fun!";
console.log(motivation);

// checking data type
let motive="Javascript";
console.log(typeof(motive));

// numbers
let number=34;
console.log(number);

// Boolean (true or false)
let isStudent=true;
let isTrainer=false;
console.log(isStudent);
console.log(isTrainer);

// Null (empty on purpose)
let ExplorerClass=null;
console.log(ExplorerClass);
console.log(typeof(ExplorerClass));

let myFutureCar;
console.log(myFutureCar);
myFutureCar="Mercedes";
console.log(myFutureCar);

// let can be reassigned

// const cannot be reassigned

//Javascript is case sensitive

const email="esther@gmail.com";
console.log(email);

