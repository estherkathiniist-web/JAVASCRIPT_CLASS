// COMPARISON OPERATORS
//compare values and return a boolean(true or false)

// equal to(==)...eg. comparing if 4 is equal to 4
console.log(4==4);
console.log(4==3);

// strict equal (===)
// checks both value and datatype
console.log(4==="4");
console.log(4===4);

// not equal to(!=)
console.log(4!=4);

// strictly not equal to(!==)
console.log(4!==4);

// greater than(>)
console.log(4>5);

// less than (<)
console.log(4<5);

// greater or equal to(>=)
console.log(4>=5);

// less than or equal to (<=)
console.log(4<=5);

