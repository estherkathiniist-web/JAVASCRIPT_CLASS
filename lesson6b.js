// SWITCH AND BREAK STATEMENT

let grade = "C";

switch (grade) {
  case "A":
    console.log("Exellent Work!");
    break;
  case "B":
    console.log("Good job!");
    break;
  case "C":
    console.log("You can do  better than this!");
    break;
  case "D":
    console.log("Put more effort!");
    break;
  case "E":
    console.log("Pull up your socks!");
    break;
  default:
    console.log("Please see the teacher in charge");
}

